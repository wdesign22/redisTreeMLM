<?php

namespace Eugene\Blog2Bundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * post2
 *
 * @ORM\Table(name="post2")
 * @ORM\Entity(repositoryClass="Eugene\Blog2Bundle\Repository\post2Repository")
 */
class post2
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="content", type="text")
     */
    private $content;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return post2
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }
}

